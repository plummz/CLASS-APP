create or replace function public.class_app_claim_code_lab_point(
  p_challenge_id text,
  p_challenge_date date
)
returns text
language plpgsql
security definer
set search_path = public
as $$
declare
  actor text := public.class_app_username();
begin
  if actor is null then
    raise exception 'Login required';
  end if;

  insert into public.code_lab_completions (username, challenge_id, challenge_date, points)
  values (actor, p_challenge_id, p_challenge_date, 1)
  on conflict (username, challenge_id, challenge_date) do nothing;

  if found then
    return 'claimed';
  end if;
  return 'already_claimed';
end;
$$;

create or replace function public.class_app_code_lab_leaderboard()
returns table(username text, total_points bigint)
language sql
stable
security definer
set search_path = public
as $$
  select c.username, sum(c.points)::bigint as total_points
  from public.code_lab_completions c
  group by c.username
  order by total_points desc, c.username asc;
$$;

grant execute on function public.class_app_claim_code_lab_point(text, date) to anon, authenticated;
grant execute on function public.class_app_code_lab_leaderboard() to anon, authenticated;

