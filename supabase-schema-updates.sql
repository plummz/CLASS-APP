create or replace function public.class_app_contribution_tally()
returns table(username text, total bigint)
language sql
stable
security definer
set search_path = public
as $$
  select files.uploader::text as username, count(*)::bigint as total
  from public.files
  where coalesce(files.is_original_upload, true) = true
    and nullif(files.uploader, '') is not null
  group by files.uploader
  order by total desc, username asc;
$$;

grant execute on function public.class_app_contribution_tally() to anon, authenticated;

create or replace function public.class_app_can_view_folder(folder_row public.folders)
returns boolean
language sql
stable
as $$
  select
    not (
      coalesce(folder_row.folder_type, '') = 'profile'
      or coalesce(folder_row.parent, '') like 'profile:%'
    )
    or folder_row.owner = public.class_app_username()
    or public.class_app_is_admin()
    or coalesce(folder_row.permissions->>'everyone', 'edit') in ('view', 'edit')
    or coalesce(folder_row.permissions->'viewers', '[]'::jsonb) ? public.class_app_username()
    or coalesce(folder_row.permissions->'editors', '[]'::jsonb) ? public.class_app_username();
$$;

create or replace function public.class_app_can_edit_folder(folder_row public.folders)
returns boolean
language sql
stable
as $$
  select
    public.class_app_is_admin()
    or folder_row.owner = public.class_app_username()
    or (
      not (
        coalesce(folder_row.folder_type, '') = 'profile'
        or coalesce(folder_row.parent, '') like 'profile:%'
      )
      and public.class_app_username() is not null
    )
    or coalesce(folder_row.permissions->>'everyone', 'edit') = 'edit'
    or coalesce(folder_row.permissions->'editors', '[]'::jsonb) ? public.class_app_username();
$$;

create or replace function public.class_app_can_place_folder(folder_parent text, folder_owner text)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select
    public.class_app_is_admin()
    or (
      folder_owner = public.class_app_username()
      and (
        folder_parent is null
        or folder_parent = ''
        or folder_parent = 'profile:' || public.class_app_username()
        or (
          folder_parent not like 'profile:%'
          and not exists (
            select 1 from public.folders parent_folder
            where parent_folder.id::text = folder_parent
          )
        )
        or exists (
          select 1 from public.folders parent_folder
          where parent_folder.id::text = folder_parent
            and public.class_app_can_edit_folder(parent_folder)
        )
      )
    );
$$;

